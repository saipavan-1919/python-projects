""" 
    lambda function
    Lambda function is known as anonymous function that doesn't have a name.
    Lambda function contains multiple arguments and single expression
    syntax : lambda <arguments> : <arguments> : <expression>
    * Only one expression
    * performs expression and returns the value
"""

add = lambda x,y: x+y
print(add(2,3))

div = lambda x,y: x/y
print(div(6,3))
